#!/usr/bin/env python3
"""
Identify de novo N-X-S/T sequons generated by ProteinMPNN.

This script scans designed sequences for sequons that don't exist in the native
structure, answering: "Does ProteinMPNN actively avoid creating sequons?"

Author: Laura Dillon
Date: 2026-01-23
"""

import pandas as pd
import numpy as np
from pathlib import Path
from Bio import SeqIO
from collections import defaultdict
import json

# Paths
BASE_DIR = Path(__file__).parent.parent.parent.parent
MANIFEST_FILE = BASE_DIR / "data/glyco_benchmark/manifests/glycoprotein_details_simple.csv"
DESIGNS_DIR = BASE_DIR / "data/glyco_benchmark/designs"
OUTPUT_DIR = BASE_DIR / "experiments/09_de_novo_sequon_generation/results"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

def is_sequon(triplet):
    """Check if a triplet is an N-X-S/T sequon (X != P)."""
    if len(triplet) != 3:
        return False
    return (triplet[0] == 'N' and
            triplet[1] != 'P' and
            triplet[2] in ['S', 'T'])

def load_manifest():
    """Load protein manifest with native sequences."""
    df = pd.read_csv(MANIFEST_FILE)
    return df

def load_designs(pdb_id, chain_id):
    """Load designed sequences from unconstrained FASTA file."""
    fasta_file = DESIGNS_DIR / f"{pdb_id}_{chain_id}_unconstrained.fasta"

    if not fasta_file.exists():
        return []

    designs = []
    for record in SeqIO.parse(fasta_file, "fasta"):
        designs.append(str(record.seq))

    return designs

def find_sequons_in_sequence(sequence):
    """Find all N-X-S/T sequons in a sequence."""
    sequons = []
    for pos in range(len(sequence) - 2):
        triplet = sequence[pos:pos+3]
        if is_sequon(triplet):
            sequons.append({
                'position': pos,
                'triplet': triplet
            })
    return sequons

def identify_de_novo_sequons(native_seq, designs):
    """Identify sequons that appear in designs but not in native."""
    # Filter designs to match native sequence length
    valid_designs = [d for d in designs if len(d) == len(native_seq)]

    if not valid_designs:
        print(f"  Warning: No designs match native length ({len(native_seq)})")
        return [], [], []

    if len(valid_designs) < len(designs):
        print(f"  Warning: {len(designs) - len(valid_designs)} designs filtered due to length mismatch")

    # Find native sequons
    native_sequons = find_sequons_in_sequence(native_seq)
    native_sequon_positions = set(s['position'] for s in native_sequons)

    # Storage
    de_novo_sequons = []
    all_design_sequons = []

    # Check each design
    for design_id, design_seq in enumerate(valid_designs):
        # Find all sequons in this design
        design_sequons = find_sequons_in_sequence(design_seq)

        for sequon in design_sequons:
            pos = sequon['position']
            triplet = sequon['triplet']

            all_design_sequons.append({
                'design_id': design_id,
                'position': pos,
                'designed_triplet': triplet
            })

            # Check if this position had a sequon in native
            if pos not in native_sequon_positions:
                # This is a de novo sequon!
                native_triplet = native_seq[pos:pos+3]

                de_novo_sequons.append({
                    'design_id': design_id,
                    'position': pos,
                    'native_triplet': native_triplet,
                    'designed_triplet': triplet,
                    'was_native_sequon': False
                })

    return de_novo_sequons, all_design_sequons, native_sequons

def calculate_random_expectation(sequence_length, n_designs):
    """Calculate expected number of sequons by random chance."""
    # Probability of N-X-S/T at any position
    # P(N) = 1/20, P(X != P) = 18/20, P(S or T) = 2/20
    prob_sequon = (1/20) * (18/20) * (2/20)

    # Number of possible triplet positions
    n_positions = sequence_length - 2

    # Expected sequons per design
    expected_per_design = n_positions * prob_sequon

    # Total expected across all designs
    total_expected = expected_per_design * n_designs

    return {
        'prob_sequon_per_position': prob_sequon,
        'n_positions_per_design': n_positions,
        'expected_per_design': expected_per_design,
        'total_expected': total_expected,
        'total_positions_scanned': n_positions * n_designs
    }

def main():
    """Main analysis pipeline."""

    print("="*70)
    print("DE NOVO SEQUON GENERATION ANALYSIS")
    print("="*70)
    print()

    # Load manifest
    print("Loading protein manifest...")
    manifest = load_manifest()
    print(f"Found {len(manifest)} proteins\n")

    # Storage
    all_de_novo = []
    all_design_sequons = []
    protein_summaries = []

    total_designs = 0
    total_positions = 0
    total_de_novo = 0
    total_expected = 0

    # Process each protein
    for idx, row in manifest.iterrows():
        pdb_id = row['pdb_id']
        chain_id = row['chain_id']
        native_seq = row['sequence']

        if pd.isna(native_seq) or len(native_seq) == 0:
            print(f"Skipping {pdb_id}_{chain_id} - no sequence in manifest")
            continue

        print(f"Processing {pdb_id}_{chain_id} ({len(native_seq)} residues)...")

        # Load designs
        designs = load_designs(pdb_id, chain_id)
        if not designs:
            continue

        print(f"  Loaded {len(designs)} designs")

        # Find de novo sequons
        de_novo, design_sequons, native_sequons = identify_de_novo_sequons(native_seq, designs)

        # Calculate random expectation for this protein
        expected = calculate_random_expectation(len(native_seq), len(designs))

        print(f"  Native sequons: {len(native_sequons)}")
        print(f"  De novo sequons found: {len(de_novo)}")
        print(f"  Expected by chance: {expected['total_expected']:.1f}")
        print(f"  Ratio (observed/expected): {len(de_novo)/expected['total_expected']:.3f}")

        # Store results
        for dn in de_novo:
            dn['protein'] = f"{pdb_id}_{chain_id}"
            dn['sequence_length'] = len(native_seq)
            all_de_novo.append(dn)

        for ds in design_sequons:
            ds['protein'] = f"{pdb_id}_{chain_id}"
            all_design_sequons.append(ds)

        protein_summaries.append({
            'protein': f"{pdb_id}_{chain_id}",
            'length': len(native_seq),
            'n_designs': len(designs),
            'n_native_sequons': len(native_sequons),
            'n_de_novo_sequons': len(de_novo),
            'expected_de_novo': expected['total_expected'],
            'observed_over_expected': len(de_novo) / expected['total_expected'] if expected['total_expected'] > 0 else np.nan,
            'total_positions_scanned': expected['total_positions_scanned']
        })

        total_designs += len(designs)
        total_positions += expected['total_positions_scanned']
        total_de_novo += len(de_novo)
        total_expected += expected['total_expected']

        print()

    # Save detailed results
    print("Saving detailed results...")
    if all_de_novo:
        de_novo_df = pd.DataFrame(all_de_novo)
        de_novo_df.to_csv(OUTPUT_DIR / "de_novo_sequons_detailed.csv", index=False)
    else:
        print("Warning: No de novo sequons found!")

    if all_design_sequons:
        design_sequons_df = pd.DataFrame(all_design_sequons)
        design_sequons_df.to_csv(OUTPUT_DIR / "all_design_sequons.csv", index=False)

    summary_df = pd.DataFrame(protein_summaries)
    summary_df.to_csv(OUTPUT_DIR / "protein_de_novo_summary.csv", index=False)

    # Calculate overall statistics
    print("\n" + "="*70)
    print("OVERALL RESULTS")
    print("="*70)

    print(f"\nDataset:")
    print(f"  Total proteins: {len(summary_df)}")
    print(f"  Total designs: {total_designs}")
    print(f"  Total triplet positions scanned: {total_positions:,}")

    print(f"\nDe Novo Sequon Generation:")
    print(f"  Observed de novo sequons: {total_de_novo}")
    print(f"  Expected by random chance: {total_expected:.1f}")
    print(f"  Ratio (observed/expected): {total_de_novo/total_expected:.3f}")

    observed_rate = 100 * total_de_novo / total_positions
    expected_rate = 100 * (1/20) * (18/20) * (2/20)  # 0.45%

    print(f"\nGeneration Rates:")
    print(f"  Observed: {observed_rate:.3f}% per triplet position")
    print(f"  Expected: {expected_rate:.3f}% per triplet position")
    print(f"  Rate ratio: {observed_rate/expected_rate:.3f}")

    # Statistical test (Binomial test)
    from scipy import stats

    prob_sequon = (1/20) * (18/20) * (2/20)
    binom_test = stats.binomtest(total_de_novo, total_positions, prob_sequon, alternative='two-sided')

    print(f"\nStatistical Test (Binomial):")
    print(f"  p-value: {binom_test.pvalue:.4e}")

    if binom_test.pvalue < 0.05:
        if total_de_novo < total_expected:
            print(f"  ✓ ProteinMPNN ACTIVELY AVOIDS generating sequons (p < 0.05)")
        else:
            print(f"  ✓ ProteinMPNN generates MORE sequons than expected (p < 0.05)")
    else:
        print(f"  No significant difference from random expectation (p >= 0.05)")

    # Interpretation
    print(f"\n" + "="*70)
    print("INTERPRETATION")
    print("="*70)

    ratio = total_de_novo / total_expected

    if ratio < 0.5:
        print(f"\nObserved/Expected ratio is {ratio:.2f} (<< 1.0)")
        print(f"→ ProteinMPNN STRONGLY AVOIDS generating N-X-S/T sequons de novo.")
        print(f"→ This confirms ACTIVE BIAS against sequon motifs.")
        print(f"→ Combined with low retention, this is strong evidence of anti-sequon bias.")
    elif ratio < 0.9:
        print(f"\nObserved/Expected ratio is {ratio:.2f} (< 1.0)")
        print(f"→ ProteinMPNN generates sequons LESS than random expectation.")
        print(f"→ This suggests MODERATE BIAS against sequon motifs.")
    elif ratio > 1.1:
        print(f"\nObserved/Expected ratio is {ratio:.2f} (> 1.0)")
        print(f"→ ProteinMPNN generates sequons MORE than random expectation!")
        print(f"→ This is unexpected and warrants further investigation.")
    else:
        print(f"\nObserved/Expected ratio is {ratio:.2f} (≈ 1.0)")
        print(f"→ ProteinMPNN generates sequons at the RANDOM EXPECTATION rate.")
        print(f"→ No evidence of bias in de novo generation.")
        print(f"→ Low sequon retention may be due to other factors (e.g., structural context).")

    # Save summary statistics
    summary_stats = {
        'total_proteins': len(summary_df),
        'total_designs': int(total_designs),
        'total_positions_scanned': int(total_positions),
        'observed_de_novo_sequons': int(total_de_novo),
        'expected_de_novo_sequons': float(total_expected),
        'observed_over_expected_ratio': float(total_de_novo / total_expected),
        'observed_rate_percent': float(observed_rate),
        'expected_rate_percent': float(expected_rate),
        'rate_ratio': float(observed_rate / expected_rate),
        'binomial_p_value': float(binom_test.pvalue)
    }

    with open(OUTPUT_DIR / "summary_statistics.json", 'w') as f:
        json.dump(summary_stats, f, indent=2)

    print(f"\nResults saved to: {OUTPUT_DIR}")
    print(f"  - de_novo_sequons_detailed.csv")
    print(f"  - all_design_sequons.csv")
    print(f"  - protein_de_novo_summary.csv")
    print(f"  - summary_statistics.json")
    print("\nDone!")

if __name__ == "__main__":
    main()
